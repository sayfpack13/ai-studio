// Get all models in a flat array
export const getAllModels = () => {
  return Object.values(models).flatMap(category => 
    category.models.map(model => ({
      ...model,
      category: category.id,
      categoryName: category.name
    }))
  );
};

// Get models by category
export const getModelsByCategory = (categoryId) => {
  return models[categoryId]?.models || [];
};

// Get model by ID
export const getModelById = (modelId) => {
  return getAllModels().find(m => m.id === modelId);
};

// Get free models
export const getFreeModels = () => {
  return getAllModels().filter(m => m.free);
};

// Get categories
export const getCategories = () => {
  return Object.values(models).map(({ id, name, description, icon }) => ({
    id,
    name,
    description,
    icon
  }));
};
