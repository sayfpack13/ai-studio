// Helper to parse model ID and extract provider/name
const parseModel = (id) => {
  const isFree = id.includes(':free');
  const cleanId = id.replace(':free', '').replace(':exacto', '');
  const parts = cleanId.split('/');
  
  let provider = 'Unknown';
  let name = id;
  
  if (parts.length >= 2) {
    provider = parts[parts.length - 2];
    name = parts[parts.length - 1];
  } else {
    name = parts[0];
  }
  
  // Format name nicely
  name = name.replace(/-/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
  
  return { id, name, provider, free: isFree };
};

